public class ifelseifadder {
    public static void main(String[] args) {
        int markes = 75;

        if (markes >= 75)
            System.out.println("Garde A");
        else if (markes >= 75)
            System.out.println("Grade B");
        else if (markes >= 50)
            System.out.println("Grade C");
        else
            System.out.println("Fail");

    }

}
